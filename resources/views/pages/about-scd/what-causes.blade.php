                <h5 class="px-4 pt-25 text-black">Try to be well-informed. Be aware of these basics:</h5>
                <p class="px-4 text-black fs-6">Misinformation causes harm to people who are born with sickle cell disease. <br>
                    So, it is important to understand what sickle cell disease IS, medically, and what it IS NOT.
                </p>
                <div class="px-4 pt-2">
                    <ul class="ps-4 mb-0">
                        <li class="mb-2 fs-6">•	Just like eye color or skin color, sickle cell disease is caused by the GENES.</li>
                        <li class="mb-2 fs-6">•	Therefore, sickle cell disease is classified as a GENETIC disease.</li>
                        <li class="mb-2 fs-6">•	A baby’s sickle cell genes are the genes that they inherit from their TWO birth parents.</li>
                        <li class="mb-2 fs-6">•	So, individuals are BORN with sickle cell disease. You CANNOT “catch” it.</li>
                       <li class="mb-2 fs-6">•	It is not a “curse.” And it is no one’s “fault.”</li>
                       <li class="mb-2 fs-6">•	It is the result of a natural process. But it can cause much pain and suffering.</li>
                       <li class="mb-2 fs-6">•	Good medical treatment can help precious individuals lead fulfilling lives.</li>
                    </ul>
                </div>


