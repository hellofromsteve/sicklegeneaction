                <h4 class="pt-10">Hydroxyurea (HU) – A Disease-Altering Drug Available Through a Public-Private Partnership</h4>
                <h5 class="pt-20">2018-2022: Ghana’s Groundwork for Providing Hydroxyurea (2018-2022)</h5>

                <div class="text-justify pt-10" >
                    <p class="text-black">Prof. Ohene-Frempong championed a partnership between the Ghana government and Novartis that was launched in 2019 to improve SCD diagnosis and care including establishing new SCD centres and expanding affordable access to hydroxyurea therapy.</p>
                </div>

                <div class="text-justify pt-10" >
                    <p class="text-black">In 2018, in preparation for the establishment in 2019 of a broad-based Public Private Partnership in SCD involving the Ghana government and Novartis, a group of parents asked Novartis to provide HU at a lower price for use in Ghana. </p>
                </div>

                <h5 class="pt-20">The Public-Private Partnership lead to a number of important outcomes, including: </h5>

                <div class="accordion pt-40" id="hydroxyureaAccordion">

                    <!-- 1. Initial Source -->
                    <div class="accordion-item">
                        <h2 class="accordion-header" id="heading0">
                            <button class="accordion-button collapsed" type="button" data-bs-toggle="collapse" data-bs-target="#collapse0" aria-expanded="false" aria-controls="collapse0">
                                • An Initial Source of Hydroxyurea
                            </button>
                        </h2>
                        <div id="collapse0" class="accordion-collapse collapse" aria-labelledby="heading0" data-bs-parent="#hydroxyureaAccordion">
                            <div class="accordion-body">
                                The initial start-up donation of hydroxyurea to the Government of Ghana by Novartis. Novartis produced 500mg capsule of HU and submitted it to Ghana Food and Drugs Authority registered the medicine for the specific indication of SCD in October 2018.
                            </div>
                        </div>
                    </div>

                    <!-- 2. Ahodwo Program -->
                    <div class="accordion-item">
                        <h2 class="accordion-header" id="heading1">
                            <button class="accordion-button collapsed" type="button" data-bs-toggle="collapse" data-bs-target="#collapse1" aria-expanded="false" aria-controls="collapse1">
                                • The Ahodwo Program [pronounced A-HU(n)-jo]
                            </button>
                        </h2>
                        <div id="collapse1" class="accordion-collapse collapse" aria-labelledby="heading1" data-bs-parent="#hydroxyureaAccordion">
                            <div class="accordion-body">
                                The Sickle Cell Foundation of Ghana (SCFG), a partner in the Public-Private Partnership was tasked to develop the Ghana-Novartis Hydroxyurea-for-SCD Program. The program was named the “Ahodwo” meaning a “Program for Relief” in Twi, one Ghanaian language.
                            </div>
                        </div>
                    </div>

                    <!-- 3. Treatment Protocol -->
                    <div class="accordion-item">
                        <h2 class="accordion-header" id="heading2">
                            <button class="accordion-button collapsed" type="button" data-bs-toggle="collapse" data-bs-target="#collapse2" aria-expanded="false" aria-controls="collapse2">
                                • The Hydroxyurea Treatment Protocol
                            </button>
                        </h2>
                        <div id="collapse2" class="accordion-collapse collapse" aria-labelledby="heading2" data-bs-parent="#hydroxyureaAccordion">
                            <div class="accordion-body">
                                A team of Ghanaian Sickle Cell Disease experts developed an HU-for-SCD dose-escalating, maximum tolerated dose Ahodwo Protocol adapted for Ghana.
                            </div>
                        </div>
                    </div>

                    <!-- 4. Ahodwo App -->
                    <div class="accordion-item">
                        <h2 class="accordion-header" id="heading3">
                            <button class="accordion-button collapsed" type="button" data-bs-toggle="collapse" data-bs-target="#collapse3" aria-expanded="false" aria-controls="collapse3">
                                • The Ahodwo Program App
                            </button>
                        </h2>
                        <div id="collapse3" class="accordion-collapse collapse" aria-labelledby="heading3" data-bs-parent="#hydroxyureaAccordion">
                            <div class="accordion-body">
                                In order to register and guide Healthcare Professionals on the protocol, register all subjects, assist with dosing calculations, and monitor the entire program, a secure, smartphone-based mobile application, Ahodwo Program App, was developed, tested, and deployed to all Healthcare Professionals in the program. Recording toxicity and reporting all expected/unexpected adverse events were mandated and reportable through the App.
                            </div>
                        </div>
                    </div>

                    <!-- 5. Treatment Teams -->
                    <div class="accordion-item">
                        <h2 class="accordion-header" id="heading4">
                            <button class="accordion-button collapsed" type="button" data-bs-toggle="collapse" data-bs-target="#collapse4" aria-expanded="false" aria-controls="collapse4">
                                • Hydroxyurea Treatment Teams at Centres Around Ghana
                            </button>
                        </h2>
                        <div id="collapse4" class="accordion-collapse collapse" aria-labelledby="heading4" data-bs-parent="#hydroxyureaAccordion">
                            <div class="accordion-body">
                                Doctor-nurse-pharmacist teams were recruited and trained from 11 Treatment Centres from different regions in Ghana and began distribution of HU in early 2020. The number of Treatment Centres expanded to 21, and additional centres were expected to be onboarded by the end of 2022.
                            </div>
                        </div>
                    </div>

                    <!-- 6. Steering Committee -->
                    <div class="accordion-item">
                        <h2 class="accordion-header" id="heading5">
