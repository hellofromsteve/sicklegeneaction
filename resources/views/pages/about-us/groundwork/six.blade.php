                                <p>✓ <strong>Purpose:</strong> It was created to develop and support services for people living with sickle cell disease.</p>

                                <p>✓ <strong>Vision for a Sickle Cell and Blood Center:</strong> Alongside the Sickle Cell Association of Ghana, the Foundation prioritized the establishment of the Center for Sickle Cell Disease in Kumasi.</p>
