                                                            <button class="accordion-button collapsed text-primary" type="button" data-bs-toggle="collapse" data-bs-target="#researchCollapse">
                                                                <strong>3. To Provide a Base for Africa-Centered Research</strong>
                                                            </button>
                                                        </h2>
                                                        <div id="researchCollapse" class="accordion-collapse collapse" data-bs-parent="#purposeSubAccordion">
                                                            <div class="accordion-body px-3">
                                                                <p>✓ <strong>An International Genetic Disorder:</strong> Sickle cell disease is fundamentally the same disorder in all parts of the world.</p>

                                                                <p>✓ <strong>The Special Needs in Africa:</strong> However, the local conditions in Africa, particularly the ubiquitous influence of malaria, make the clinical manifestation of SCD different in patients who live in Africa.</p>

                                                                <p>✓ <strong>The Need for Africa-Centered Research:</strong> For this and other reasons, treatment options recommended in the United States and elsewhere may be inapplicable to, inappropriate, or insufficient for patients in Africa.</p>
                                                                <div class="ms-3">
                                                                    <p>▪ <strong>a. Difficult economic conditions:</strong> The economic conditions in much of Africa make expensive, technologically demanding treatment regimens impractical for most patients. This includes cures and certain treatments.</p>
                                                                    <p>▪ <strong>b. Preventable suffering and loss of life:</strong> Until an easy and inexpensive treatment or cure is found for SCD, all centers managing a large number of patients must engage in some research to improve the health and lives of the patients.</p>
                                                                </div>

                                                                <p>✓ <strong>The Research-Based Origin of the Center:</strong> The SCD initiative in Kumasi originated from a research grant from the National Institutes of Health of the U.S.</p>
                                                                <div class="ms-3">
                                                                    <p>▪ <strong>a. The Essential Role of Research:</strong> A well-organized Center for Sickle Cell Disease in Kumasi will have research as one of its major foci of activity.</p>
                                                                    <p>▪ <strong>b. A Strong Research Base for International Support:</strong> The Center will maintain and strengthen the links established with research institutions abroad in order to be able to compete effectively for international support for local research activities.</p>
