                                                                <strong>2. To Provide Education and Training</strong>
                                                            </button>
                                                        </h2>
                                                        <div id="educationCollapse" class="accordion-collapse collapse" data-bs-parent="#purposeSubAccordion">
                                                            <div class="accordion-body px-3">
                                                                <p>✓ <strong>A Complex Illness:</strong> Sickle cell disease is a complex illness with multi-system involvement and a wide range of potentially fatal complications.</p>
                                                                <p>✓ <strong>A High Burden of Disease:</strong> Even though Africa has the highest number of patients with this disease, health care for SCD patients in Africa has lagged far behind that in countries with well-organized programs.</p>
                                                                <p>✓ <strong>The Need for Well-Trained Personnel:</strong> The main reason for the difference in care for patients in Africa vs. care for patients in other countries may be a lack of material resources. However, inadequately trained personnel is another contributing factor. One of the main goals of the Center for Sickle Cell Disease in Kumasi is to develop and implement national and regional training programs in the diagnosis and management of SCD.</p>
                                                                <p>✓ <strong>Training Goals:</strong></p>
                                                                <div class="ms-3">
                                                                    <p>▪ <strong>a.</strong> The Center aims to train doctors, nurses, laboratory technicians, counselors, and others in SCD program development as well as patient care.</p>
                                                                    <p>▪ <strong>b.</strong> In addition, the Center is to provide education, testing, and genetic counseling related to sickle cell conditions for members of the general public.</p>
                                                                </div>
                                                            </div>
                                                        </div>
                                                    </div>

