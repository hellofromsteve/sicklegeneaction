                                                    <!-- Sub Accordion 1: Clinical Care -->
                                                    <div class="accordion-item">
                                                        <h2 class="accordion-header">
                                                            <button class="accordion-button collapsed text-primary" type="button" data-bs-toggle="collapse" data-bs-target="#clinicalCareCollapse">
                                                                <strong>1. To Provide Clinical Care</strong>
                                                            </button>
                                                        </h2>
                                                        <div id="clinicalCareCollapse" class="accordion-collapse collapse" data-bs-parent="#purposeSubAccordion">
                                                            <div class="accordion-body px-3">
                                                                <p>✓ <strong>A Serious Chronic Condition:</strong> Sickle cell disease is a chronic debilitating disease.</p>
                                                                <p>✓ <strong>The Need for Comprehensive Care:</strong> Like all chronic diseases, the best health care requires long-term comprehensive management involving a multi-disciplinary team of doctors, nurses, health educators and counselors, social workers, and laboratory technicians organized to meet the multiple needs of the patients and their families.</p>
                                                                <p>✓ <strong>The Need for a Specialized Clinical Care Team:</strong> This team is to be available to the patient for routine outpatient services as well as acute and inpatient care.</p>
                                                                <p>✓ <strong>Precedents from Other Countries:</strong> Such a comprehensive approach has proven very successful in the management of SCD in many countries (U.S., U.K., France, Jamaica, Brazil, Saudi Arabia, Kuwait, and others) where there has been a well-organized approach to the management of the disease.</p>
                                                                <p>✓ <strong>A Need to Avoid Fragmented Care:</strong> The very large number of people with SCD in Ghana’s urban communities has made it impractical to organize their care through fragmented outpatient clinics.</p>
                                                                <p>✓ <strong>The Presence of an Existing Clinical Site:</strong> By 2006, the Sickle Cell Clinic at the Komfo Anokye Teaching Hospital (KATH) was serving more than 10,000 patients and had grown by more than 700 patients per year for the past five years, since the initial proposal for the Sickle Cell and Blood Center in Kumasi.</p>
                                                                <p>✓ <strong>The Need to Expand:</strong> Prof. Ohene-Frempong felt that a facility that is available to serve patients on a regular basis would be the ideal setting for such a large population.</p>
                                                                <p>✓ <strong>A Possible Medical Home for Patients in Kumasi and Surrounding Areas:</strong> The Center for Clinical Genetics at Korle Bu Teaching Hospital in Accra had been created to provide a medical home for their large numbers of patients with SCD in Accra. The proposed Kumasi Center has been under construction to serve a similar purpose for patients in Kumasi and surrounding areas.</p>
                                                            </div>
                                                        </div>
                                                    </div>

